package Railworld_India_PVT_Task;

public class StringBuffer_ConcatenatingString {
    public static void main(String[] args) {
        StringBuffer sb = new StringBuffer();
        sb.append("Deepika");
        sb.append(" ");
        sb.append("Solanki");
        String result = sb.toString();
        System.out.println(result);
    }
}
